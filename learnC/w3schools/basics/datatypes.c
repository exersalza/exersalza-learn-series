#include <stdio.h>

int main() {
	// int 2 or 4 bytes
	// long 8 bytes
	// long long 16 bytes
	// float 4 bytes
	// double 8 bytes
	// long double 16 bytes
	// char 1 byte
	// bool 1 byte
	// void 0 bytes

	int myNum = 5;
	float myFloat = 5.99;
	char myLetter = 'D';

	printf("My number is %d\n", myNum);
	printf("My float is %f\n", myFloat);
	printf("My letter is %c\n", myLetter);
}
